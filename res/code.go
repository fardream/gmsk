// res package contains the MSKrescodee
// of MOSEK. This is to prevent the so many codes
// from poluting the name space of gmsk.
package res

// #include<stdlib.h>
// #include <mosek.h>
import "C"

import "unsafe"

// MSK_MAX_STR_LEN is the max length of strings in mosek
const MAX_STR_LEN = C.MSK_MAX_STR_LEN

// Code is the return code from MOSEK routines
type Code uint32

// IsOk checks if the result is ok.
func (c Code) IsOk() bool {
	return c == OK
}

// GetDesc set the content of sym and desc just like MSK_getcodedesc.
// However, the size of the input slice must be greater than or equal to MAX_STR_LEN + 1
// The returned bool indicate if the size of sym/desc is greater than [MAX_STR_LEN].
// The function will do nothing if sym and desc are smaller than or equal to [MAX_STR_LEN].
func (c Code) GetDesc(sym []byte, desc []byte) (Code, bool) {
	if len(sym) <= MAX_STR_LEN || len(desc) <= MAX_STR_LEN {
		return 0, false
	}

	symstart := unsafe.Pointer(&sym[0])
	descstart := unsafe.Pointer(&desc[0])
	r := C.MSK_getcodedesc(C.MSKrescodee(c), (*C.char)(symstart), (*C.char)(descstart))

	return Code(r), true
}

// GetDescription gets the description of the return code.
// Please check [Code] to see if the operation is successful.
// The first returned string is the symbol,
// and the second returned string is the description.
// This is different from [Code.GetDesc] and involves allocating and freeing two char array of
// [MAX_STR_LEN] + 1 size
func (c Code) GetDescription() (Code, string, string) {
	symbol := (*C.char)(C.calloc(MAX_STR_LEN+1, C.sizeof_char))
	defer C.free(unsafe.Pointer(symbol))
	des := (*C.char)(C.calloc(MAX_STR_LEN+1, C.sizeof_char))
	defer C.free(unsafe.Pointer(des))

	r := C.MSK_getcodedesc(C.MSKrescodee(c), symbol, des)
	return Code(r), C.GoString(symbol), C.GoString(symbol)
}

